package pyrimadprogram;

public class reversepyramid {

	public static void main(String[] args) {
		int rows =5;
		for(int i= rows;i>=1;i--) {
			for(int j=1;j<=i; j++) {
				System.out.print("* ");//print *
			}
			System.out.println();
		}
		
		int row =5;
		for(int i= row;i>=1;i--) {
			for(int j=1;j<=i; j++) {
				System.out.print(j+" ");  //print1.2..
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		

}}
